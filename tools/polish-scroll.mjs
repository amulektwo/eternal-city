/* ── the laws, each a pure function ─────────────────────────────────── */

/** L-a: strip YAML frontmatter block. */
export function cutYaml(t) {
  return t.replace(/^\s*---\r?\n[\s\S]*?\r?\n---\r?\n?/, "");
}

/** L-b: strip provenance blocks (## Source … up to the next heading), plus
 *  stray provenance bullets and the bare '## Extracted Body' marker. */
export function cutSourceBlocks(t) {
  let out = t.replace(/##+\s*Source\b[\s\S]*?(?=\n##|##\s*Extracted|$)/gi, "");
  out = out.replace(/##+\s*Extracted Body\s*/gi, "");
  // extraction-pipeline tail sections (plumbing around the scripture)
  out = out.replace(/##+\s*Cross-References\b[\s\S]*?(?=\n##|$)/gi, "");
  out = out.replace(/##+\s*Required Review\b[\s\S]*?(?=\n##|$)/gi, "");
  out = out.replace(
    /^[-*]?\s*\*{0,2}(?:Source (?:Mountain|Thread(?: ID)?|Project|Platform)|Extraction Type|Body Status|Source Status|Doctrinal Confidence)\s*:?\*{0,2}.*$/gim,
    ""
  );
  // flattened (single-line) variant, e.g. inside excerpts
  out = out.replace(
    /-?\s*\*\*(?:Source (?:Mountain|Thread(?: ID)?|Project|Platform)|Extraction Type|Body Status)\s*:\*\*[^-#]*/gi,
    " "
  );
  return out;
}

/** L-c: strip raw thread UUIDs. */
export function cutUuids(t) {
  return t.replace(UUID, "");
}

/** L-d + L-e: strip self-talk / competition sentences. */
export function cutSelfTalk(t) {
  let out = t;
  for (const re of SELF_TALK) out = out.replace(re, "");
  return out;
}

/** L-f: strip attribution tag lines. */
export function cutAttribution(t) {
  let out = t;
  for (const re of ATTRIBUTION) out = out.replace(re, "");
  return out;
}

/** L-g: strip process-audit / collision lines. */
export function cutProcess(t) {
  let out = t;
  for (const re of PROCESS_CUTS) out = out.replace(re, "");
  for (const re of FORGE_FIELDS) out = out.replace(re, "");
  return out;
}

/** L-h: strip chat turn-headers, keep the prose beneath them. */
export function cutTurnHeaders(t) {
  return t.replace(/^#{1,6}[ \t]*\[(?:User|Assistant|Human|AI)\][ \t]*:?[ \t]*$/gim, "");
}

/** L-i: de-plumb LightRAG — [[Scroll_0019]] → Scroll 0019; archive-entry
 *  clauses cut whole. */
export function cutLightRag(t) {
  let out = t.replace(LIGHTRAG_LINE, "");
  // wikilinks nest ([[[[SCROLL 656]]]].2B]]) — resolve to the fixed point
  for (let pass = 0; pass < 6 && /\[\[/.test(out); pass++) {
    const prev = out;
    out = out.replace(/\[\[Scroll[_ ]?([^\][|]+?)\]\]/gi, (_, id) =>
      ("Scroll " + String(id).replace(/_/g, " ")).trim()
    );
    out = out.replace(/\[\[([^\][|]+?)\]\]/g, "$1");
    if (out === prev) break;
  }
  return out;
}

/** L-j: sealing attributions + legal name (public surface: always). */
export function cutSealing(t) {
  let out = t;
  for (const re of SEALING) out = out.replace(re, "");
  return out;
}

/** L-k: em-dash in prose → comma; em-dash in STRUCTURE stays.
 *  Structure = scripture attributions (— Book 1:1), header/title lines
 *  (ALL-CAPS or SCROLL N), box/rule lines (═ ║ ╔ ⸻), quote attributions. */
export function normalizeEmDash(t) {
  const lines = t.split("\n");
  const out = lines.map((line) => {
    if (/[═║╔╚╠⸻]/.test(line)) return line; // sacred geometry untouched
    if (/^\s*(?:#{1,6}\s*)?(?:\*\*)?SCROLL\b/i.test(line)) return line; // header/heading blocks
    const letters = line.replace(/[^A-Za-z]/g, "");
    if (letters.length >= 6 && letters === letters.toUpperCase()) return line; // ALL-CAPS titles
    let s = line.replace(/\s+—\s+/g, (m, off) => {
      const rest = line.slice(off + m.length);
      return SCRIPTURE_AFTER.test(rest) ? m : ", ";
    });
    s = s.replace(/(\S)—(\S)/g, (m, a, b, off) => {
      const rest = line.slice(off + m.length - 1);
      return SCRIPTURE_AFTER.test(rest) ? m : `${a}, ${b}`;
    });
    return s;
  });
  return out.join("\n");
}

/** tidy: collapse the scars the cuts leave behind. */
function tidy(t) {
  return t
    .replace(/[ \t]+\n/g, "\n")
    .replace(/\n{4,}/g, "\n\n\n")
    .replace(/[ \t]{2,}/g, " ")
    .replace(/^\s*[-•]\s*$/gm, "")
    .replace(/\b(of|by|to|through|with|for)\s+,\s*/gi, "$1 ") // "voice of , the Seer"
    .replace(/\s+,/g, ",")
    .replace(/,{2,}/g, ",")
    .trim();
}

