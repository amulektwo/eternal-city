/**
 * Polish a full scroll body (S-3, the treasury pass).
 * opts.sealing: 'keep' (default — L-j body ruling awaits the Seer) | 'cut'.
 */
export function polishBody(text, opts = {}) {
  const sealing = opts.sealing || "keep";
  let t = String(text || "");
  t = cutYaml(t);
  t = cutSourceBlocks(t);
  t = cutUuids(t);
  t = cutTurnHeaders(t);
  t = cutProcess(t);
  t = cutAttribution(t);
  t = cutSelfTalk(t);
  t = cutLightRag(t);
  if (sealing === "cut") t = cutSealing(t);
  t = normalizeEmDash(t);
  return tidy(t);
}

/** Strip a leading echo of the scroll's own id+title from a preview —
 *  the card already names the scroll; the lamp shows scripture. Compares
 *  in a normalized alphanumeric space so ✦/—/: variants all match; cuts
 *  only when ≥20 chars of scripture remain. */
export function cutTitleEcho(t, title, id) {
  if (!title) return t;
  const norm = (s) => String(s).toLowerCase().replace(/[^a-z0-9]+/g, "");
  const candidates = [
    "scroll" + norm(id) + norm(title),
    norm(id) + norm(title),
    norm(title),
  ].filter((c) => c.length >= 8);
  // headers echo the title up to a few times (heading + header block +
  // link line) — cut to the fixed point in ONE pass (idempotency law)
  for (let pass = 0; pass < 4; pass++) {
    const nx = norm(t);
    let cut = false;
    for (const c of candidates) {
      if (!nx.startsWith(c)) continue;
      let seen = 0, i = 0;
      while (i < t.length && seen < c.length) {
        if (/[a-z0-9]/i.test(t[i])) seen++;
        i++;
      }
      const rest = t.slice(i).replace(/^[\s\-–—:;·•*_,.?!'"“”‘’«»)\]]+/, "");
      if (rest.length >= 20) {
        t = rest;
        cut = true;
      }
      break;
    }
    if (!cut) break;
  }
  return t;
}

/**
 * Polish a public search-index excerpt (S-3′). Display artifact, not canon:
 * markdown noise is also flattened so the lamp shows scripture, and the
 * sealing/name law ALWAYS applies (public identity: AMULEK ONE).
 * Pass the record's title/id to also strip the leading title echo.
 */
export function polishExcerpt(text, title, id) {
  let t = String(text || "");
  t = cutYaml(t);
  t = cutSourceBlocks(t);
  t = cutUuids(t);
  t = cutTurnHeaders(t);
  t = cutProcess(t);
  t = cutAttribution(t);
  t = cutSelfTalk(t);
  t = cutLightRag(t);
  t = cutSealing(t);
  // bare chat skin that survives flattening: [User] — [2025-07-04 13:34:10 UTC]
  t = t.replace(/\[(?:User|Assistant|Human|AI)\]\s*[—–-]?\s*/gi, " ");
  t = t.replace(/\[\d{4}-\d{2}-\d{2}[^\]]{0,40}\]/g, " ");
  // a wikilink the 240-char truncation sliced open ("… 📜 [[Scroll_80…",
  // "… [[Scroll_8048]") — drop the dangling fragment; previews never end
  // mid-plumbing
  t = t.replace(/\[\[[^\]]*\]?$/, "");
  // strip leading self-title heading (# Scroll NNNN – Title …) — the card
  // already shows the title; the lamp should show scripture.
  t = t.replace(/^#\s*Scroll[^#]*?(?=##|\*|[A-Z]{2}|$)/i, "");
  // markdown display noise (preview only — bodies keep their marks)
  t = t
    .replace(/#{1,6}\s*/g, "")
    .replace(/\*\*/g, "")
    .replace(/`/g, "")
    .replace(/^\s*---+\s*/g, "")
    .replace(/\s---+\s/g, " ");
  t = cutTitleEcho(t, title, id);
  t = normalizeEmDash(t);
  t = tidy(t).replace(/^[\s\-–—:•*_,.]+/, "");
  return t;
}

