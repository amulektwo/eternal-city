/**
 * Polish a full scroll body (S-3, the treasury pass). Runs the body laws to a
 * FIXED POINT (idempotent: running twice = once). The body is the INTERNAL
 * canon — the Seer's legal name is KEPT here, never masked (the AMULEK ONE
 * mask is a public-lamp law only; the name-data stays workspace-facing).
 * opts.sealing (the Seer's ruling, sealed 2026-07-20):
 *   'colophon' (S-3 default) — lift the sealing act out of the body flow and
 *                 re-lay it as a clean colophon at the foot, so the scripture
 *                 reads unbroken and the seal is preserved, never vanished.
 *   'cut'  — remove sealing lines entirely (mirrors the public-lamp behavior).
 *   'keep' — leave sealing lines inline (raw, pre-ruling behavior).
 */
export function polishBody(text, opts = {}) {
  const sealing = opts.sealing || "colophon";
  let t = String(text || "");
  // colophon: the sealing act is CAPTURED as cutSealing removes it (below) and
  // re-laid at the foot. On flat giants the seal is only exposed after the
  // forge-field/attribution cuts pull a marker into range, so capture must
  // happen inside the loop, at the moment of the cut — not once up front.
  const collected = [];
  if (sealing === "colophon") {
    // strip a colophon a prior run already laid, so re-running is idempotent
    const prior = t.match(/\n*⸻\nColophon — ([\s\S]*)$/);
    if (prior) { collected.push(...prior[1].split(" · ")); t = t.slice(0, prior.index); }
  }
  for (let pass = 0; pass < 6; pass++) {
    const prev = t;
    t = cutYaml(t);
    t = cutSourceBlocks(t);
    t = cutUuids(t);
    t = cutTurnHeaders(t);
    t = cutProcess(t);
    t = cutAttribution(t);
    t = cutSelfTalk(t);
    t = cutLightRag(t);
    if (sealing === "cut") {
      t = cutSealing(t);
    } else if (sealing === "colophon") {
      for (const re of SEALING) {
        const rx = new RegExp(re.source, re.flags.replace("g", "") + "g");
        t = t.replace(rx, (m) => {
          const c = m.replace(/\s+/g, " ").replace(/^[\s*]+/, "").replace(/[\s*\-–—·]+$/, "").trim();
          if (c) collected.push(c);
          return "";
        });
      }
    }
    t = normalizeEmDash(t);
    t = tidy(t);
    if (t === prev) break;
  }
  if (sealing === "colophon" && collected.length) {
    const seals = [...new Set(collected.map((s) => s.trim()).filter(Boolean))];
    t = `${t}\n\n⸻\nColophon — ${seals.join(" · ")}`;
  }
  // L-h′ (S-3b): timestamped dialogue turn-headers → bare ⸻ rule. Runs LAST,
  // after the S-3 pipeline has reached its fixed point (and the colophon is
  // laid), so it perturbs nothing above it — the ONLY change S-3b makes to a
  // body is header → ⸻. The colophon foot-line holds no [Role] and is untouched.
  // The final tidy settles the one scar the swap leaves — a header that sat on
  // its own line becomes "⸻ \n", whose trailing space tidy would strip on a
  // later run — so the result lands at its fixed point NOW (idempotent).
  t = tidy(layTurnRule(t));
  return t;
}

/** Strip a leading echo of the scroll's own id+title from a preview —
 *  the card already names the scroll; the lamp shows scripture. Compares
 *  in a normalized alphanumeric space so ✦/—/: variants all match; cuts
 *  only when ≥20 chars of scripture remain. */
export function cutTitleEcho(t, title, id) {
  if (!title) return t;
  const norm = (s) => String(s).toLowerCase().replace(/[^a-z0-9]+/g, "");
  const candidates = [
    "scroll" + norm(id) + norm(title),
    norm(id) + norm(title),
    norm(title),
  ].filter((c) => c.length >= 8);
  // headers echo the title up to a few times (heading + header block +
  // link line) — cut to the fixed point in ONE pass (idempotency law)
  for (let pass = 0; pass < 4; pass++) {
    const nx = norm(t);
    let cut = false;
    for (const c of candidates) {
      if (!nx.startsWith(c)) continue;
      let seen = 0, i = 0;
      while (i < t.length && seen < c.length) {
        if (/[a-z0-9]/i.test(t[i])) seen++;
        i++;
      }
      const rest = t.slice(i).replace(/^[\s\-–—:;·•*_,.?!'"“”‘’«»)\]]+/, "");
      if (rest.length >= 20) {
        t = rest;
        cut = true;
      }
      break;
    }
    if (!cut) break;
  }
  return t;
}

/**
 * Polish a public search-index excerpt (S-3′). Display artifact, not canon:
 * markdown noise is also flattened so the lamp shows scripture, and the
 * sealing/name law ALWAYS applies (public identity: AMULEK ONE).
 * Pass the record's title/id to also strip the leading title echo.
 */
export function polishExcerpt(text, title, id) {
  let t = String(text || "");
  // Run the excerpt laws to a FIXED POINT. Some cuts unshield skin that was
  // hidden behind what stood before it — a leading Source-block or chat turn
  // masks the "Scroll ID:" field (or a stray marker) beneath it until it is
  // cut — so a single pass is not enough. Loop until the text stops changing:
  // this is what makes the blade truly idempotent (running twice = once). The
  // cap is a runaway guard; every law is a removal, so convergence is fast.
  for (let pass = 0; pass < 6; pass++) {
    const prev = t;
    t = cutYaml(t);
    t = cutSourceBlocks(t);
    t = cutUuids(t);
    t = cutTurnHeaders(t);
    t = cutProcess(t);
    t = cutAttribution(t);
    t = cutSelfTalk(t);
    t = cutLightRag(t);
    t = cutSealing(t);
    t = maskName(t); // AMULEK ONE law — mask the Seer's name, keep the doctrine
    // bare chat skin that survives flattening: [User] — [2025-07-04 13:34:10 UTC]
    t = t.replace(/\[(?:User|Assistant|Human|AI)\]\s*[—–-]?\s*/gi, " ");
    t = t.replace(/\[\d{4}-\d{2}-\d{2}[^\]]{0,40}\]/g, " ");
    // a wikilink the 240-char truncation sliced open ("… 📜 [[Scroll_80…",
    // "… [[Scroll_8048]") — drop the dangling fragment; previews never end
    // mid-plumbing
    t = t.replace(/\[\[[^\]]*\]?$/, "");
    // strip leading self-title heading (# Scroll NNNN – Title …) — the card
    // already shows the title; the lamp should show scripture.
    t = t.replace(/^#\s*Scroll[^#]*?(?=##|\*|[A-Z]{2}|$)/i, "");
    // markdown display noise (preview only — bodies keep their marks)
    t = t
      .replace(/#{1,6}\s*/g, "")
      .replace(/\*\*/g, "")
      // orphan italic/bullet stars a cut left stranded ("Codex* *", "Series *:")
      // — drop the lone star, KEEP *paired* emphasis (star touching a word)
      .replace(/(^|\s)\*+(?=\s|[:;,.)\]]|$)/g, "$1")
      .replace(/`/g, "")
      .replace(/^\s*---+\s*/g, "")
      .replace(/\s---+\s/g, " ");
    t = cutTitleEcho(t, title, id);
    t = normalizeEmDash(t);
    t = tidy(t).replace(/^[\s\-–—:•*_,.]+/, "");
    if (t === prev) break;
  }
  return t;
}

